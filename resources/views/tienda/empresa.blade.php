<br>INDUSTRIA METALÚRGICA SAN MARCOS, fue fundada el 2 de abril de 1971, por RAMON ALDO GASTALDI además de otros integrantes de su familia.

<br/>
<br/>
La empresa se instaló en la localidad de San Marcos Sud, Provincia de Córdoba, en calle Viamonte y Urquiza a una cuadra de la vía férrea y su actividad principal: la construcción de TANQUES PARA COMBUSTIBLES - SUBTERRANEOS Y AEREOS - HORIZONTALES Y VERTICALES - TANQUES PARA RIEGO - TANQUES ATMOSFERICOS - SOBRE CAMION Y ACOPLADOS - ACOPLADOS RECOLECTORES DE RESIDUOS - CAJAS VOLCADORAS - ACOPLADOS RURALES.

<br/>
<br/>
A partir de 1975, se fabrican tanques especiales para compañías petroleras y bodegas. También para firmas particulares en diversas capacidades.

<br/>
<br/>
A partir de 1977 agrega a sus negocios la construcción de silos para campos y elevadores de campaña (llave en mano) participando desde la obra civil e incluyendo maquinarias de movimiento de granos (norias, sinfines, cintas transportadoras, elementos de limpieza, zarandas y otros) de diseño propio con instalación completa.

<br/>
<br/>
En la década del 90 comienza una etapa de cambios, modificándose la planta trasladada a independencia y corrientes s/n, construyendo SEMIRREMOLQUES ATMOSFERICOS - ACOPLADOS RECOLECTORES COMPACTADORES DE RESIDUOS - TANQUES AUTOBOMBAS COMPLETOS PARA BOMBEROS.

<br/>
<br/>
CUMPLIENDO 37 AÑOS DE COMPROMISO CON EL DESARROLLO PRODUCTIVO Y LOS VALORES INDUSTRIALES DE SAN MARCOS SUD CON UNA NUEVA INDUSTRIA FAMILIAR AL SERVICIO DEL PAIS.

<br/>
<br/>
